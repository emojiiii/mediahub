/// Durable lifecycle states for a backend-neutral direct upload session.
#[derive(Clone, Copy, Debug, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum UploadSessionState {
    Pending,
    Completed,
    Cancelled,
    Expired,
}

impl UploadSessionState {
    #[must_use]
    pub const fn is_terminal(self) -> bool {
        !matches!(self, Self::Pending)
    }
}

/// Result of applying a terminal action to an upload session.
#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub enum UploadSessionTransition {
    Applied,
    AlreadyApplied,
}

/// Server-side input used to create a pending upload session. The target
/// storage location is generated by an adapter before the session is made
/// durable; clients never choose it.
#[derive(Clone, Debug)]
pub struct NewUploadSession {
    pub id: UploadSessionId,
    pub media_id: MediaId,
    pub application_id: ApplicationId,
    pub bucket_id: BucketId,
    pub object_key: String,
    pub original_name: Option<String>,
    pub display_name: String,
    pub extension: Option<String>,
    pub expected_size: u64,
    pub expected_mime: String,
    pub storage_backend: String,
    pub storage_key: String,
    pub visibility_override: Option<Visibility>,
    pub media_expires_at: Option<OffsetDateTime>,
    pub client_metadata: ClientMetadata,
    pub session_expires_at: OffsetDateTime,
}

